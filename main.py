from src.models.quiz_game import QuizGame


def main() -> None:
    """Application entry point."""
    game = QuizGame()
    game.run()


# 이 파일을 직접 실행할 때만 main()을 호출한다.
# 다른 모듈에서 import될 때는 실행되지 않아 재사용/테스트가 쉬워진다.
if __name__ == "__main__":
    main()

# --- 모듈 전역(인터프리터·임포트 시스템이 채우는 이름들) ---
# 모듈이 로드될 때 전역 네임스페이스에 함께 잡히는 대표적인 이름:
#   __name__   : 모듈 이름. 이 파일을 직접 실행하면 "__main__", import되면 "main" 등.
#   __doc__    : 모듈 독스트링(없으면 None).
#   __file__   : 이 소스 파일 경로(내장 모듈 등에는 없을 수 있음).
#   __package__: 패키지 컨텍스트용 문자열(최상위 스크립트는 ""일 수 있음).
#   __loader__ : 이 모듈을 불러온 로더.
#   __spec__   : import 메타데이터(ModuleSpec).
#   __cached__ : 캐시된 .pyc 경로 등(없을 수 있음).
#   __builtins__: builtins 모듈 참조(내장 함수·타입·예외 등이 모인 이름 공간).
# 패키지 디렉터리일 때만: __path__(서브모듈 검색 경로 리스트).
#
# 변경 시 주의:
#   - 위 이름들은 도구·디버거·if __name__ == "__main__" 판별 등에 쓰이므로
#     임의로 덮어쓰면 동작이 깨지기 쉽다. 일반 앱 코드에서 바꾸지 않는 것이 좋다.
#   - 기술적으로 대입이 되는 경우가 많아도, 의도된 API가 아니며 유지보수·테스트를 어렵게 만든다.
#   - 일부 객체의 속성은 읽기 전용이라 대입 시 AttributeError가 날 수 있다.
#
# --- 파이썬(CPython) 빌드·실행과 바이트코드 캐시(.pyc) ---
# 실행 흐름(요약):
#   1) 소스(.py)를 읽어 문법 분석 → AST
#   2) AST를 바이트코드로 컴파일(기계어가 아니라 VM용 명령열)
#   3) CPython 가상 머신이 바이트코드를 한 단계씩 실행(인터프리터에 가까운 실행)
#
# .pyc 파일:
#   - 컴파일 결과인 바이트코드를 디스크에 저장한 캐시. 확장자의 "c"는 C언어가 아니라
#     관례적으로 "compiled" 캐시를 뜻하는 경우가 많다.
#   - import 시 캐시가 유효하면 소스를 다시 파싱·컴파일하지 않고 .pyc에서 바이트코드를
#     읽어 실행해 로드를 빠르게 할 수 있다.
#
# 캐시 무효화와 재컴파일:
#   - 소스가 바뀌었는지는 보통 mtime·크기 또는 소스 해시 등으로 모듈 단위로 판단한다.
#   - 한 번 무효화되면 그 모듈은 통째로 다시 컴파일한다(일부 라인만 바이트코드 재사용
#     같은 부분 단위 최적화는 일반 import 경로에 없다 — 구현 단순·정확성).
#
# __cached__:
#   - 이 모듈과 연결된 바이트코드 캐시 파일 경로가 들어갈 수 있다(없을 수도 있음).
#   - 경로가 있다고 해서 항상 그 파일만 쓴다는 뜻은 아니며, import 시스템이 유효성을
#     검사한 뒤 캐시 사용 여부를 결정한다.
